// .devnet/pca-smoke-lib.mjs — pure helpers for the issue #519 V10 PCA
// HTTP smoke (.devnet/run.mjs). Kept separate so the discount-assertion
// and evidence-report logic is unit-testable without booting a devnet.

// `KnowledgeAssetsV10.publish()` silently demotes to the no-discount
// branch when the publishing wallet is not a registered PCA agent or
// when `epochs != lockDurationEpochs` (PRD §8 risk). A genuine discount
// is `0 < discountedCost < baseCost`; equality means the branch was not
// taken even though HTTP returned 200.
export function assertDiscountTaken({ baseCost, discountedCost }) {
  const base = BigInt(baseCost);
  const disc = BigInt(discountedCost);
  if (disc <= 0n) {
    return { ok: false, reason: `discountedCost=${disc} is not positive` };
  }
  if (disc >= base) {
    return {
      ok: false,
      reason: `no discount applied (silent demotion): discountedCost=${disc} >= baseCost=${base}`,
    };
  }
  return { ok: true, reason: `discount applied: ${disc} < ${base}` };
}

// Decide what to do with one publisher wallet given its current
// on-chain `agentToAccountId` mapping (0 = unregistered). Makes the
// smoke idempotent across re-runs against a reused devnet: a wallet
// already bound to OUR account must be skipped, not re-POSTed (the
// contract reverts `AgentAlreadyRegistered`); a wallet bound to a
// different account is a hard conflict the caller must surface.
export function classifyAgentRegistration(onChainAccountId, targetAccountId) {
  const cur = BigInt(onChainAccountId);
  const tgt = BigInt(targetAccountId);
  if (cur === 0n) return { action: 'register' };
  if (cur === tgt) return { action: 'skip' };
  return {
    action: 'conflict',
    reason: `agent already bound to account ${cur}, want ${tgt}`,
  };
}

// Bind the ACTUAL publish signer to the discount assertion. The daemon
// publisher rotates among operational wallets, so the wallet that
// signed the publish tx (`receipt.from`) is not necessarily the one we
// registered first. If its on-chain `agentToAccountId` is not our
// account, KnowledgeAssetsV10 took the no-discount branch silently —
// surface that distinctly instead of as an opaque discount-math failure.
export function assertPublishSignerBound({ signer, signerAccountId, accountId }) {
  const sid = BigInt(signerAccountId);
  const tgt = BigInt(accountId);
  if (sid === 0n) {
    return {
      ok: false,
      reason: `publish signer ${signer} is not a registered agent (silent demotion: no discount branch)`,
    };
  }
  if (sid !== tgt) {
    return {
      ok: false,
      reason: `publish signer ${signer} is bound to account ${sid}, not the smoke's account ${tgt}`,
    };
  }
  return { ok: true, reason: `publish signer ${signer} is a registered agent of account ${tgt}` };
}

// Round-trip evidence written to .scratch/issue-519/verify.md (issue
// #519 TB-0007 acceptance criterion 3). One table row per scripted step.
export function buildVerifyMarkdown({ accountId, steps, passed }) {
  const verdict = passed ? 'PASS' : 'FAIL';
  const rows = steps
    .map((s) => `| ${s.name} | ${s.status} | ${s.detail} |`)
    .join('\n');
  return [
    '# Issue #519 — V10 PCA HTTP round-trip',
    '',
    `Generated by \`.devnet/run.mjs\` at ${new Date().toISOString()}`,
    '',
    `- accountId: \`${accountId}\``,
    `- verdict: **${verdict}**`,
    '',
    '| Step | Status | Detail |',
    '| --- | --- | --- |',
    rows,
    '',
  ].join('\n');
}
